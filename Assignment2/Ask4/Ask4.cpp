#include "Ask4.h"

namespace Prakseis
{  
    float maths::Add(float a, float b)
    {            
        return a + b;
    }

    float maths::Subtract(float a, float b)
    {
        return a - b;
    }

    float maths::Multiply(float a, float b)
    {
        return a * b;
    }

    float maths::Divide(float a, float b)
    {
        return a / b;
    }

}




